// Signed-distance-field grid eval + marching cubes. 1:1 port of
// frontend/_sdf_.py (`eval_sdf_grid`, `marching_cubes`).
//
// Two SDF primitives on the wire:
//   * Sphere: params = [cx, cy, cz, radius, _, _, _, _] (8 floats)
//   * Capsule: params = [p0x, p0y, p0z, bax, bay, baz, ba_dot_ba, radius]
// `sdf_types[i]` is 0 for sphere, 1 for capsule.
//
// Parallelism: rayon over the x-dimension of the grid (same as the
// Python `prange` over `i`). The marching-cubes pass is intrinsically
// sequential because of the per-cube vertex deduplication (hashmap
// keyed on (i, j, k, edge)).

use std::collections::HashMap;

use rayon::prelude::*;

const SDF_SPHERE: i32 = 0;
const SDF_CAPSULE: i32 = 1;
const PARAMS_PER_SDF: usize = 8;

// ---------------------------------------------------------------------------
// SDF primitives.

#[inline]
fn sdf_sphere(x: f64, y: f64, z: f64, p: &[f64]) -> f64 {
    let dx = x - p[0];
    let dy = y - p[1];
    let dz = z - p[2];
    (dx * dx + dy * dy + dz * dz).sqrt() - p[3]
}

#[inline]
fn sdf_capsule(x: f64, y: f64, z: f64, p: &[f64]) -> f64 {
    let pax = x - p[0];
    let pay = y - p[1];
    let paz = z - p[2];
    let bax = p[3];
    let bay = p[4];
    let baz = p[5];
    let ba_dot_ba = p[6];
    let radius = p[7];
    let pa_dot_ba = pax * bax + pay * bay + paz * baz;
    let h = (pa_dot_ba / ba_dot_ba).clamp(0.0, 1.0);
    let dx = pax - bax * h;
    let dy = pay - bay * h;
    let dz = paz - baz * h;
    (dx * dx + dy * dy + dz * dz).sqrt() - radius
}

#[inline]
fn eval_min_at(x: f64, y: f64, z: f64, sdf_types: &[i32], sdf_params: &[f64]) -> f64 {
    let mut min_dist = f64::INFINITY;
    for (s, &t) in sdf_types.iter().enumerate() {
        let p = &sdf_params[s * PARAMS_PER_SDF..(s + 1) * PARAMS_PER_SDF];
        let d = match t {
            SDF_SPHERE => sdf_sphere(x, y, z, p),
            SDF_CAPSULE => sdf_capsule(x, y, z, p),
            _ => sdf_capsule(x, y, z, p),
        };
        if d < min_dist {
            min_dist = d;
        }
    }
    min_dist
}

// ---------------------------------------------------------------------------
// Grid eval.

/// Evaluate `min` of all SDFs on the grid product (xs × ys × zs).
/// Returns a flat (nx * ny * nz) buffer in row-major C-contiguous
/// order matching numpy `(nx, ny, nz)`. Caller reshapes if needed.
///
/// `sdf_params` is flattened: 8 floats per SDF (some unused for
/// spheres). `sdf_types[i]` selects the primitive.
pub fn eval_sdf_grid(
    xs: &[f64],
    ys: &[f64],
    zs: &[f64],
    sdf_types: &[i32],
    sdf_params: &[f64],
) -> Vec<f64> {
    let nx = xs.len();
    let ny = ys.len();
    let nz = zs.len();
    let total = nx * ny * nz;
    let mut grid = vec![0.0f64; total];

    let stride_x = ny * nz;
    grid.par_chunks_mut(stride_x).enumerate().for_each(|(i, plane)| {
        let x = xs[i];
        for j in 0..ny {
            let y = ys[j];
            for k in 0..nz {
                let z = zs[k];
                plane[j * nz + k] = eval_min_at(x, y, z, sdf_types, sdf_params);
            }
        }
    });
    grid
}

